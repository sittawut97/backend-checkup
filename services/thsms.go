// generateRandomOTPCode generates a random 6-digit OTP
func generateRandomOTPCode() string {
	otp := ""
	for i := 0; i < 6; i++ {
		num, _ := rand.Int(rand.Reader, big.NewInt(10))
		otp += fmt.Sprintf("%d", num.Int64())
	}
	return otp
}

// formatThaiPhoneNumber converts Thai phone number to international format
// Input: "0812345678" -> Output: "+66812345678"
func formatThaiPhoneNumber(phone string) string {
	// Remove non-digits
	cleaned := ""
	for _, ch := range phone {
		if ch >= '0' && ch <= '9' {
			cleaned += string(ch)
		}
	}

	// If starts with 0 and length is 10, remove it and add +66
	if len(cleaned) == 10 && cleaned[0] == '0' {
		return "+66" + cleaned[1:]
	}

	// If already has +66, return as is
	if len(cleaned) == 12 && cleaned[:2] == "66" {
		return "+" + cleaned
	}

	return ""
}
