// THSMSClientImpl implements SMSClient interface for THSMS API
type THSMSClientImpl struct {
	APIToken   string
	BaseURL    string
	Sender     string
	HTTPClient *http.Client
}

// THSMSRequestPayload represents the request body for THSMS API
type THSMSRequestPayload struct {
	Sender  string   `json:"sender"`
	MSISDN  []string `json:"msisdn"`
	Message string   `json:"message"`
}

// THSMSResponsePayload represents the response from THSMS API
type THSMSResponsePayload struct {
	Status  int    `json:"status"`
	Message string `json:"message"`
	Data    struct {
		MessageID string `json:"message_id"`
		Count     int    `json:"count"`
	} `json:"data"`
}

// NewTHSMSClientImpl creates a new THSMS client
func NewTHSMSClientImpl(apiToken, baseURL, sender string) *THSMSClientImpl {
	return &THSMSClientImpl{
		APIToken: apiToken,
		BaseURL:  baseURL,
		Sender:   sender,
		HTTPClient: &http.Client{
			Timeout: 10 * time.Second,
		},
	}
}

